// Package types provides shared type definitions for the Claude Agent SDK.
//
// This package contains all public types used across the SDK, including:
//   - Message types (UserMessage, AssistantMessage, etc.)
//   - Content block types (TextBlock, ToolUseBlock, etc.)
//   - Hook types and callbacks
//   - Permission types
//   - MCP server and tool types
//   - Configuration options
//
// The types package has no dependencies on other SDK packages, allowing it
// to be imported by both public and internal packages without circular imports.
package types
